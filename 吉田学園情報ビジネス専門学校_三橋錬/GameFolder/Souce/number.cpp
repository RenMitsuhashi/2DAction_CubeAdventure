void CNumber::Update(void)
{

}

//=============================================================================
// ポリゴンの描画処理
//=============================================================================
void CNumber::Draw(void)
{
	CScene2D::Draw();
}

CNumber *CNumber::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, LPDIRECT3DTEXTURE9 tex)
{
	CNumber* pNumber = new CNumber;

	if (pNumber != NULL)
	{
		pNumber->Init(pos, size, tex);
	}
	return pNumber;
}